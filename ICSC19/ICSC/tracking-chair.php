ity, Malaysia (2014-2016), Visiting Professor Kyushu University, Japan (2005), Chemnitz University of Technology, Germany (2002), University of New South Wales, Australia (2000-2001), Visiting Senior Scientist in the Advanced Telecommunication Research (ATR) Institute, Kyoto, Japan (2000), Visiting Professor in Ecole Nationale d’Ingenieurs de Brest, France (1998), JSPS Visiting Professor in the Kyushu University, Japan (1997-98) and NASA Summer Professor in the Kennedy Space Center, Florida in 1997 and 1999.  
<br><br>
Dr. Rawat has been listed in “Who’s Who in Technology Today”, “The International Directory of Distinguished Leadership”, “Who’s Who in Electromagnetics”, Who’s Who in America”, “American Men and Women of Science”, “Who’s Who Among Asian Americans”, “Who’s Who in the World” and “Who’s Who in Science and Engineering”.  He was invited by the United Nations as an International Expert in Microwaves and Optical Technology under TOKTEN and UNIDO programs in 1989, 1992 and 1994.  He has Chaired/Organized many national/international symposia/conferences and has been the Keynote Speaker, Plenary Session Chair, Technical Program Chair and Editor of several Conference Proceedings.  He is an Advisory Board Member and Chapter author for the “Electrical Engineering Handbook”, published by CRC Press/IEEE Press.  He has been reviewer for IEEE, IEE, Intl. Journal of Electronics, Journal of Electromagnetic Waves & Applications and Indian Jr of Radio and Space Physics and book reviewer for John Wiley, McGraw-Hill, Prentice Hall and other publishers.  Dr. Rawat has been Chair of IEEE Northern Nevada Section (1990-1997).  He is the Founder/Chair of the “International Symposium on Microwave and Optical Technology (ISMOT)”, Founder/Chair of the IEEE MTTS/PS Chapter of Northern Nevada since 2000, Founder/Editor-in-Chief of the online “International Journal of Microwave and Optical Technology (IJMOT)” since 2005 and  Founder/President of the International Academy of Microwave and Optical Technology (IAMOT) since 2012. Dr. Rawat was awarded the First Aditya Vikram Birla Chair-2013 of Aditya Birla Training & Research Center, India  for outstanding contribution in Academics and Research. He is the recipient of IEEE Region-6 Central Area “Outstanding Engineer of the Year Award - 2012” and IEEE Region-6 Central Area “Outstanding Leadership and Professional Service Award-2014”.
