                    <h3 class="entry-title" style="font-weight: 700;text-align: center;">ICSC - 2018</h3>
					<p style="font-size: 15px;font-weight: 300;text-align:justify;">Signal processing consists of representation and transformation of signals and the information that they carry. Over the years, signal processing has immensely developed to provide phenomenal growth in the area of microchips, digital systems and computer hardware.</p><p style="font-size: 15px;font-weight: 300;text-align: justify;/* text-align:justify; */">
					The applications of signal processing are vast and interdisciplinary-ranging from engineering to economics and astronomy to biology. The research in areas such as signal coding and de-noising have paved the path for these advancements.</p><p style="font-size: 15px;font-weight: 300;text-align: justify;/* text-align:justify; */">
					Recent decade has witnessed major revolution in communication and processing of digital media. As a consequence, solution to major problems in processing, transmission and reception have made signal processing an integral part of modern electronic and communication systems.
					Communication on the other hand plays major role in our day to day life. The communication technology helps in the advancement of modern systems to meet our need of efficient exchange of ideas. The current research includes the areas of cognitive radio and IOT. </p>
		<p style="font-size: 15px;font-weight: 300;text-align: justify;/* text-align:justify; */">There is a need for providing a forum to scientists and researchers to discuss and put forward their ideas and research findings with the co-researchers from all over the world. ICSC 2018 will provide an opportunity to highlight recent developments and to identify emerging and future areas of growth in these fields. It will further give impetus to the researchers for proposing novel efficient techniques.</p><p style="font-size: 15px;font-weight: 300;text-align: justify;/* text-align:justify; */">
		Keynote speakers with their vast research experience in various research fields will generate interest among participants for development of signal processing and communication engineering technology. With the participation of several experts and their diverse areas of research, it is expected that the conference will help in meeting the future challenges of industry, academia and research.</p>
